    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        String nome = txtNome.getText();
        PilotoDAO dao = new PilotoDAO();
        boolean status = dao.conectar();
        if(status == true){
            Piloto p = dao.consultar(nome);
            if(p == null){
                JOptionPane.showMessageDialog(null,"Piloto não encontrado");
            }else{
                txtId.setText(String.valueOf(p.getIdPiloto()));
                txtAltura.setText(String.valueOf(p.getAltura()));
                txtPeso.setText(String.valueOf(p.getPeso()));
                DateFormat df = new SimpleDateFormat("dd/MM/yyyy");
                String dataFormatada = df.format(p.getDataNascimento()); //converteu a data no padrão BR
                txtData.setText(dataFormatada);
                btnExcluir.setEnabled(true);
            }
            dao.desconectar();
        }else{
            JOptionPane.showMessageDialog(null,"Piloto não encontrado");
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

