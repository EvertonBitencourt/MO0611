    public void preencherTabela(){
        PilotoDAO dao = new PilotoDAO();
        dao.conectar();
        String nomePiloto = txtNome.getText();
        List<Piloto> listaPilotos = dao.getPilotos(nomePiloto);
        DefaultTableModel tabelaPilotos = (DefaultTableModel) tblPilotos.getModel();
        tblPilotos.setRowSorter(new TableRowSorter(tabelaPilotos));
        tabelaPilotos.setNumRows(0);
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        for(Piloto p : listaPilotos){
            Object[] obj = new Object[]{
                p.getIdPiloto(),
                p.getNome(),
                p.getAltura(),
                p.getPeso(),
                (p.getDataNascimento() != null ? sdf.format(p.getDataNascimento()) : "")
            };
            tabelaPilotos.addRow(obj);
            
        }
        
    }

